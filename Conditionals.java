public class Conditionals {
    public static void main(String[] args)
    {
        int x = 5;
        int y = 7;
        int z = 9;

        if(x > y && x > z)
            System.out.println("X is Greatest");
        else if(y > x && y > z)
            System.out.println("Y is Greatest");
        else 
            System.out.println("Z is Greatest");


    }
}
